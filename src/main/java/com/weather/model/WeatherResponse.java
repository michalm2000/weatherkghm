package com.weather.model;

public class WeatherResponse {
    String iconB64;
    String temp;

    public String getIconB64() {
        return iconB64;
    }

    public void setIconB64(String iconB64) {
        this.iconB64 = iconB64;
    }

    public String getTemp() {
        return temp;
    }

    public void setTemp(String temp) {
        this.temp = temp;
    }
}
